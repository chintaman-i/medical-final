    function displayProducts(products) {
      const productList = document.getElementById('product-list');
      productList.innerHTML = ''; // Clear existing products
  
      products.forEach(product => {
          // Create product card
          const productCard = document.createElement('div');
          productCard.className = 'product-card';
  
          // Product image
          const productImage = document.createElement('img');
          productImage.src = product.Image_url;
          productImage.alt = product.name;
  
          // Product name
          const productName = document.createElement('h3');
          productName.textContent = product.name;
  
          // Product price
          const productPrice = document.createElement('p');
          productPrice.textContent = `Price: ₹${product.price}`;
  
          // Add to cart button
          const addToCartButton = document.createElement('button');
          addToCartButton.className = 'add-to-cart';
          addToCartButton.textContent = 'Add to Cart';
  
          // Add event listener to the "Add to Cart" button
          addToCartButton.addEventListener('click', () => addToCart(product));
  
          // Append elements to product card
          productCard.appendChild(productImage);
          productCard.appendChild(productName);
          productCard.appendChild(productPrice);
          productCard.appendChild(addToCartButton);
  
          // Append product card to product list
          productList.appendChild(productCard);
      });
  }
  
  // Function to add product to cart
  function addToCart(product) {
      // Retrieve the current cart from localStorage or initialize an empty array
      let cart = JSON.parse(localStorage.getItem('cart')) || [];
  
      // Check if the product is already in the cart
      let existingItem = cart.find(item => item.name === product.name);
  
      if (existingItem) {
          // If product already exists, increment its quantity
          existingItem.quantity += 1;
      } else {
          // If product is new to the cart, set default quantity to 1
          const cartItem = {
              name: product.name,
              price: product.price,
              quantity: 1 // Set default quantity to 1
          };
          cart.push(cartItem);
      }
  
      // Save the updated cart to localStorage
      localStorage.setItem('cart', JSON.stringify(cart));
  
      // Update cart count (optional)
      updateCartCount();
  
      // Display a message indicating the product was added to the cart (optional)
      showAddToCartMessage(product.name);
  }
  
  // Function to update cart count in the header (optional)
  function updateCartCount() {
      const cart = JSON.parse(localStorage.getItem('cart')) || [];
      const totalPrice = cart.reduce((acc, item) => acc + parseFloat(item.price * item.quantity), 0);
      document.getElementById('cart-count').textContent = `Cart (${cart.length} items) - ₹${totalPrice.toFixed(2)}`;
  }
  
  // Function to show the "Added to Cart" message (optional)
  function showAddToCartMessage(productName) {
      const message = document.createElement('div');
      message.className = 'cart-message';
      message.textContent = `${productName} has been added to your cart!`;
  
      document.body.appendChild(message);
  
      setTimeout(() => {
          document.body.removeChild(message);
      }, 3000);
  }
  
